def load_naturaleza(self):
        """‚ö†Ô∏è Carga las naturalezas de cuentas (d√©bito/cr√©dito)"""
        self.stdout.write("Cargando naturalezas de cuenta...")
        
        try:
            with transaction.atomic():
                puc_class = [
                    ('DB', 'DEBITO'),
                    ('CR', 'CREDITO')
                ]
                
                count = 0
                for code, name in puc_class:
                    _, created = Naturaleza.objects.get_or_create(
                        code=code,
                        defaults={'name': name}
                    )
                    if created:
                        count += 1
                        
                self.stdout.write(self.style.SUCCESS(
                    f"Naturalezas de cuenta cargadas: {count} nuevas, {len(puc_class)-count} existentes"
                ))
        except Exception as e:
            self.stdout.write(self.style.ERROR(f"Error al cargar naturalezas: {str(e)}"))
            raise

